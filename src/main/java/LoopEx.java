public class LoopEx {
    public static void main(String[] args) {

        for (int i = 0; i < 10; i++) {
            System.out.println("hellow, world");
        }
        System.out.println();

        for (int i = 1; i <= 10; i++) {
            System.out.println("hellow, world");
        }
    }
}