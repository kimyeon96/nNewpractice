public class OperationEx {
    public static void main(String[] args) {

        int num1 = 5;
        int num2 = 10;

        int result = num1 & num2;
        System.out.println(result);

        int num3 = 5;
        int num4 = 11;
        int result2 = num3 | num4;
        System.out.println(result2);
    }
}
